#include <stdio.h>

/*
描述
明明生成了
?
N个1到500之间的随机整数。请你删去其中重复的数字，即相同的数字只保留一个，把其余相同的数去掉，然后再把这些数从小到大排序，按照排好的顺序输出。

数据范围： 
1-1000
输入的数字大小满足 ：1-500 

输入描述：
第一行先输入随机整数的个数 N 。 接下来的 N 行每行输入一个整数，代表明明生成的随机数。 具体格式可以参考下面的"示例"。
输出描述：
输出多行，表示输入数据处理后的结果
案例输入： 
3
2
2
1
输出：
1
2
*/
/*
解析思路，通过空间换取效率一个数组，梭哈完事儿 

*/
int main() {
    int a[1001];
    int n;
    int temp;
    while (scanf("%d",&n)!=EOF) {
        for(int i=0;i<1001;i++){
            a[i]=0;
        }

        for(int i=0;i<n;i++){
            scanf("%d",&temp);
            a[temp]=1;
        }

        for(int i=0;i<1001;i++){
            if(a[i]==1){
                printf("%d\n",i);
            }
        }
    }
    return 0;
}





